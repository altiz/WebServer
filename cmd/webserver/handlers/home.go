package handlers

import (
	_ "WebServer/cmd/webserver/docs" // docs is generated by Swag CLI, you have to import it.
	"WebServer/cmd/webserver/models"
	"WebServer/version"
	"net/http"

	"github.com/gin-gonic/gin"
	// swagger embed files
)

// HomeHandlers godoc
// @Summary Текущая версия API
// @Produce json
// @Success 200 {object} models.TVersion
// @Router /home/ [post]
func HomeHandlers(c *gin.Context) {
	var vers models.TVersion
	vers.BuildTime = version.BuildTime
	vers.Commit = version.Commit
	vers.Release = version.Release
	c.JSON(http.StatusOK, vers)

}
